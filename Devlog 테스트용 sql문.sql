    v_daily := TRUNC(DBMS_RANDOM.VALUE(1, v_total + 1));  -- 1 ~ TOTAL

    MERGE INTO VISIT_COUNT v
    USING (SELECT m AS MEMBER_NO FROM dual) s
    ON (v.MEMBER_NO = s.MEMBER_NO)
